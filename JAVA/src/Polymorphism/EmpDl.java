package Polymorphism;

public class EmpDl {
	
	private int empId = 123;
	private String empName = "Nitish";
	private int empMob = 987654321;
	private int empAC = 999999999;
	
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public int getEmpMob() {
		return empMob;
	}
	public void setEmpMob(int empMob) {
		this.empMob = empMob;
	}
	public int getEmpAC() {
		return empAC;
	}
	public void setEmpAC(int empAC) {
		this.empAC = empAC;
	}

}
